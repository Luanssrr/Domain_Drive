# #Escreva um algoritmo em Python que recebe dois números inteiros e exibe: a soma desses
# dois números, a multiplicação, a divisão inteira e o resto da divisão inteira.


num1 = float(input("Digite o primeiro numero: "));
num2 = float(input("Digite o primeiro numero: "));


print("A soma dos numero", num1, " + " ,num2 , "=", (num1 + num2))
print("A multiplicação dos numero", num1, " * " ,num2 , "=", (num1 * num2))
print("A divisão inteira dos numero", num1, " / " ,num2 , "=", (num1 // num2))
print("O resto inteira dos numero", num1, " % " ,num2 , "=", (num1 % num2))
